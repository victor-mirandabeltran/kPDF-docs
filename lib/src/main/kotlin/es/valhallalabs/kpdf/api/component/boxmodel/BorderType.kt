package es.valhallalabs.kpdf.api.component.boxmodel

/**
 * Enum class representing the different types of borders that can be applied in the box model.
 *
 * @property NONE Indicates no border.
 * @property SOLID Indicates a solid line border.
 * @property DOTTED Indicates a dotted line border.
 * @property DASHED Indicates a dashed line border.
 */
enum class BorderType {
	NONE,
	SOLID,
	DOTTED,
	DASHED
}